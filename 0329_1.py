# 彩虹
# 画图框架
import turtle as t
t.bgcolor('skyblue')
t.speed(0)
t.pensize(10)


    


t.color('red')
t.left(90)
t.circle(180,180)
t.right(250)
t.forward(10)
t.left(160)

t.color('orange')
t.left(90)
t.circle(170,-180)
t.left(90)
t.forward(10)
t.right(180)

t.color('yellow')
t.left(90)
t.circle(160,180)
t.right(250)
t.forward(10)
t.left(160)

t.color('green')
t.left(90)
t.circle(150,-180)
t.left(90)
t.forward(8)
t.right(180)

t.color('indigo')
t.left(90)
t.circle(141,180)
t.right(250)
t.forward(10)
t.left(160)

t.color('blue')
t.left(90)
t.circle(132,-180)
t.left(90)
t.forward(8)
t.right(180)

t.color('purple')
t.left(90)
t.circle(123,180)

t.color('white')
t.penup()
t.goto(200,200)
t.pendown()
t.begin_fill()
t.left(86)
t.forward(100)
t.circle(40,240)
t.right(135)
t.circle(55,260)
t.end_fill()
t.forward(85)
t.done()